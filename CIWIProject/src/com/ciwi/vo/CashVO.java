package com.ciwi.vo;

public class CashVO {
	private int cash_no;
	private String cash_id;
	private String cash_date;
	private int cash_pay;
	private int cash_state;
	
	public int getCash_no() {
		return cash_no;
	}
	public void setCash_no(int cash_no) {
		this.cash_no = cash_no;
	}
	public String getCash_id() {
		return cash_id;
	}
	public void setCash_id(String cash_id) {
		this.cash_id = cash_id;
	}
	public String getCash_date() {
		return cash_date;
	}
	public void setCash_date(String cash_date) {
		this.cash_date = cash_date;
	}
	public int getCash_pay() {
		return cash_pay;
	}
	public void setCash_pay(int cash_pay) {
		this.cash_pay = cash_pay;
	}
	public int getCash_state() {
		return cash_state;
	}
	public void setCash_state(int cash_state) {
		this.cash_state = cash_state;
	}
	
}
